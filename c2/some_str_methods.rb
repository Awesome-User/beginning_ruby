# some common string methods: 

puts " \"Hello\".+(\"Hello\") is #{"Hello" .+("Hello")}"

puts " \"Hello\".upcase is #{"Hello".upcase}"

puts " \"Hello\".downcase is #{"Hello".downcase}"

puts " \"Hello\".swapcase is #{"Hello".swapcase}"

puts " \"Hello\".chop is #{"Hello".chop}" # deletes last char

puts " \"Hello\".sum is #{"Hello".sum}"

puts " \"hello\".sum is #{"hello".sum}"

puts " \"Hello\".sum + 2 is #{"Hello".sum + 2}"

puts " \"Hello\".next is #{"Hello".next}" # increments last char

puts " \"Hello\".reverse.chop is #{"Hello".reverse.chop}"

puts " \"Hello\".reverse.next is #{"Hello".reverse.next}"